-- Generate sessions for each pact (16 sessions over 37 days)
-- This is a simplified version - in reality, you'd calculate exact dates
DO $$
DECLARE
    pact_record RECORD;
    session_date DATE;
    session_time TIME;
    session_count INTEGER;
BEGIN
    FOR pact_record IN SELECT id, number, schedule FROM pacts LOOP
        session_count := 1;
        session_date := '2024-01-15'::DATE; -- Start date for Genesis Cohort
        
        -- Extract time from schedule (simplified - assumes format like "Mon/Wed/Fri at 7:00 PM GMT")
        session_time := '19:00:00'::TIME; -- Default to 7 PM
        
        -- Generate 16 sessions (roughly 3 per week for 5-6 weeks)
        WHILE session_count <= 16 LOOP
            INSERT INTO sessions (pact_id, scheduled_date, scheduled_time, session_number)
            VALUES (pact_record.id, session_date, session_time, session_count);
            
            session_count := session_count + 1;
            session_date := session_date + INTERVAL '2 days'; -- Simplified spacing
        END LOOP;
    END LOOP;
END $$;
